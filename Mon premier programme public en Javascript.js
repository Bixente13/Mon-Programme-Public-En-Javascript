function calculMoyenne(tableau) {
    let somme = 0;
    for (let i = 0; i < tableau.length; i++) {
        somme += tableau[i];
    }
    return somme / tableau.length;
}

function calculVariance(tableau) {
    let moyenne = calculMoyenne(tableau);
    let sommeCarres = 0;
    for (let i = 0; i < tableau.length; i++) {
        sommeCarres += Math.pow(tableau[i] - moyenne, 2);
    }
    return sommeCarres / tableau.length;
}

function calculEcartType(tableau) {
    return Math.sqrt(calculVariance(tableau));
}

function CalculFactorielle(n) {
    if (n === 0 || n === 1) { // Si n est égal à 0 ou 1, on retourne 1
        return 1;
    } 
    else 
    { 
        let produit = 1; // On initialise le produit à 1
        for (i = n; i >0; i--) 
            { // On initialise la valeur i à n et on décrémente i de 1 pour arriver jusqu'à 0
            produit *= i; // On multiplie le produit par i
            }
            return produit; // On retourne le produit           
    }
}


function CalculCoefficientBinomial(n, k) { 
    if (k > n) { // Si k est supérieur à n, on retourne 0
        return 0;
    }   
    if (k === 0 || k === n) { // Si k est égal à 0 ou n, on retourne 1
        return 1;           
    }
    else return CalculFactorielle(n) / (CalculFactorielle(k) * CalculFactorielle(n - k)); // On retourne le coefficient binomial
}

